    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // login
        // Name and Password = admin
        
    try {
            // Using PreparedStatement to prevent SQL injection
            String sql = "SELECT * FROM admin WHERE adminname=? AND password=?";
            PreparedStatement pstmt = con.prepareStatement(sql);
            pstmt.setString(1, jTextField1.getText());
            pstmt.setString(2, new String(jPasswordField1.getPassword())); // Getting password as char array

            ResultSet rs = pstmt.executeQuery();

            // Checking if the login is successful
            if (rs.next()) {
                JOptionPane.showMessageDialog(this, "Login Successful");
                AdminPanel obj = new AdminPanel();
                obj.setSize(1000, 700);
                obj.setVisible(true);
            } else {
                JOptionPane.showMessageDialog(this, "Login Unsuccessful, Try Again");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        } finally {
            try {
                // Closing resources in a finally block
                if (rs != null) {
                    rs.close();
                }
                if (stmt != null) {
                    stmt.close();
                }
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

