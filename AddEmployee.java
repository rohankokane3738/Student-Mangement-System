    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //ADD Employee
         String firstname = jTextField1.getText();
    String lastname = jTextField2.getText();
    String className = jTextField3.getText();
    String age = jTextField4.getText();
    String dob = jTextField5.getText();
    String contact = jTextField6.getText();
    String emailid = jTextField7.getText();
    
    String gender = "";
    if(jRadioButton1.isSelected()) {
        gender = "Male";
    } else if(jRadioButton2.isSelected()) {
        gender = "Female";
    }
    
    String permanentAddress = jTextArea1.getText();
    
    try {
        Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3306/student", "root", "root");
        Statement stmt = connection.createStatement();

        String sql = "INSERT INTO employee (firstname, lastname, class, year, dob, contact, emailid, gender, permanent_address) " +
                     "VALUES ('"+firstname+"','"+lastname+"','"+className+"','"+age+"','"+dob+"','"+contact+"','"+emailid+"','"+gender+"','"+permanentAddress+"')";
        stmt.executeUpdate(sql);
        JOptionPane.showMessageDialog(this, "Record Saved Successfully");
        
        stmt.close();
        connection.close();
    } catch(Exception ex) {
        JOptionPane.showMessageDialog(this, ex.getMessage());
    }
    }//GEN-LAST:event_jButton1ActionPerformed

